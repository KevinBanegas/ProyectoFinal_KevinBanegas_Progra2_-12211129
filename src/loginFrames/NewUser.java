    private void label_ingresar_iniciarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_label_ingresar_iniciarMouseClicked
        if (textf_contra_iniciar.getText().equals(textf_contraVer_crear.getText()) && !textf_usuario_iniciar.getText().equals("Usuario") && !textf_contra_iniciar.getText().equals("Contrase침a") && !textf_contraVer_crear.getText().equals("Reingrese Contrase침a")) {
            int ver = 0;
            for (Cuenta usuario : cuentas) {
                if (usuario.getUser().equals(textf_usuario_iniciar.getText())) {
                    ver = 1;
                }
            }
            boolean contraVer = Regex(textf_contra_iniciar.getText());
            if (ver == 0) {
                if (contraVer == true) {
                    JOptionPane.showMessageDialog(this, "Ingresado Exitosamente", "Ingresado", 1);
                    jLabel12.setVisible(false);
                    jLabel1.setVisible(false);
                    jLabel2.setVisible(false);
                    jLabel3.setVisible(false);
                    jLabel4.setVisible(false);
                    jLabel5.setVisible(false);

                    setVisible(false);
                    Cuenta newCuenta = new Cuenta(textf_usuario_iniciar.getText(), textf_contra_iniciar.getText());
                    cuentas.add(newCuenta);
                    int indexCuenta = cuentas.size() - 1;
                    textf_contra_iniciar.setText("Contrase침a");
                    textf_usuario_iniciar.setText("Usuario");
                    textf_contraVer_crear.setText("Reingrese Contrase침a");
                    new MenuPrincipal(indexCuenta, cuentas).setVisible(true);
                } else {
                    jLabel12.setVisible(true);
                    jLabel1.setVisible(false);
                    jLabel2.setVisible(false);
                    jLabel3.setVisible(false);
                    jLabel4.setVisible(false);
                    jLabel5.setVisible(false);
                }
            } else {
                jLabel1.setVisible(true);
                jLabel12.setVisible(false);
                jLabel2.setVisible(false);
                jLabel3.setVisible(false);
                jLabel4.setVisible(false);
                jLabel5.setVisible(false);
            }

        } else {
            if (textf_usuario_iniciar.getText().equals("Usuario") || textf_usuario_iniciar.getText().isEmpty()) {
                jLabel4.setVisible(true);
                jLabel3.setVisible(false);
                jLabel2.setVisible(false);
                jLabel5.setVisible(false);
                jLabel1.setVisible(false);
                jLabel12.setVisible(false);
            } else if (textf_contra_iniciar.getText().equals("Contrase침a") || textf_contra_iniciar.getText().isEmpty()) {
                jLabel3.setVisible(true);
                jLabel4.setVisible(false);
                jLabel2.setVisible(false);
                jLabel5.setVisible(false);
                jLabel1.setVisible(false);
                jLabel12.setVisible(false);
            } else if (textf_contraVer_crear.getText().equals("Reingrese Contrase침a") || textf_contraVer_crear.getText().isEmpty()) {
                jLabel5.setVisible(true);
                jLabel2.setVisible(false);
                jLabel3.setVisible(false);
                jLabel4.setVisible(false);
                jLabel1.setVisible(false);
                jLabel12.setVisible(false);

            } else if (!textf_contra_iniciar.getText().equals(textf_contraVer_crear.getText()) || (textf_contra_iniciar.getText().isEmpty() && textf_contraVer_crear.getText().isEmpty())) {
                jLabel2.setVisible(true);
                jLabel3.setVisible(false);
                jLabel4.setVisible(false);
                jLabel5.setVisible(false);
                jLabel1.setVisible(false);
                jLabel12.setVisible(false);
            }

        }
    }//GEN-LAST:event_label_ingresar_iniciarMouseClicked

