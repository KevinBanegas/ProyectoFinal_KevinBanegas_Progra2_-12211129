    private boolean Regex(String contra) {
        String regex = "^(?=.*[0-9])"
                + "(?=.*[a-z])(?=.*[A-Z])"
                + "(?=.*[@#$%^&+=])"
                + "(?=\\S+$).{8,20}$";
        Pattern p = Pattern.compile(regex);

        if (contra == null) {
            return false;
        }

        Matcher m = p.matcher(contra);

        return m.matches();
    }
