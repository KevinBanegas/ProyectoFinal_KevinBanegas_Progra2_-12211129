    private void label_registrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_label_registrarMouseClicked
        if (!textf_usuario_crear.getText().equals("Usuario") && !textf_contra_crear.getText().equals("ContraseÃ±a")) {
            int ver = 0;
            for (Cuenta usuario : cuentas) {
                if (usuario.getUser().equals(textf_usuario_crear.getText())) {
                    ver = 1;
                }
            }
            boolean contraVer = Regex(textf_contra_crear.getText());
            if (ver == 0) {
                if (contraVer == true) {
                    JOptionPane.showMessageDialog(this, "Usuario Agregado Exitosamente", "Agregado", 1);
                    jLabel12.setVisible(false);
                    jLabel1.setVisible(false);
                    jLabel5.setVisible(false);
                    jLabel4.setVisible(false);
                    Cuenta newCuenta = new Cuenta();
                    newCuenta.setUser(textf_usuario_crear.getText());
                    newCuenta.setContra(textf_contra_crear.getText());
                    cuentas.add(newCuenta);
                    AgregarDatosCuentas();
                    traerCuenta();
                    actualizarCB();
                    textf_contra_crear.setText("ContraseÃ±a");
                    textf_contra_crear.setEchoChar((char) 0);
                    textf_usuario_crear.setText("Usuario");
                } else {
                    jLabel1.setVisible(false);
                    jLabel12.setVisible(true);
                    jLabel5.setVisible(false);
                    jLabel4.setVisible(false);
                }
            } else {
                jLabel12.setVisible(false);
                jLabel1.setVisible(true);
                jLabel5.setVisible(false);
                jLabel4.setVisible(false);
            }

        } else {
            if (textf_usuario_crear.getText().equals("Usuario") || textf_usuario_crear.getText().isEmpty()) {
                jLabel4.setVisible(true);
                jLabel5.setVisible(false);
                jLabel1.setVisible(false);
            } else if (textf_contra_crear.getText().equals("ContraseÃ±a") || textf_contra_crear.getText().isEmpty()) {
                jLabel5.setVisible(true);
                jLabel4.setVisible(false);
                jLabel1.setVisible(false);
            }

        }
    }//GEN-LAST:event_label_registrarMouseClicked

