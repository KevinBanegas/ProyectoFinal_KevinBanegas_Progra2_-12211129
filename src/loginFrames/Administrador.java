    private boolean Regex(String contra) {
        String regex = "^(?=.*[0-9])"
                + "(?=.*[a-z])(?=.*[A-Z])"
                + "(?=.*[@#$%^&+=])"
                + "(?=\\S+$).{8,20}$";
        Pattern p = Pattern.compile(regex);

        if (contra == null) {
            return false;
        }

        Matcher m = p.matcher(contra);

        return m.matches();
    }

    public void AgregarDatosCuentas() {
        Dba db = new Dba("./DataBaseProyectoFinal.accdb");
        db.conectar();
        try {
            int index = 0;
            int cent = 0;
            while (cent == 0) {
                cent = 1;
                index++;
                for (Cuenta cuenta : cuentas) {
                    if (index == cuenta.getId()) {
                        cent = 0;
                    }
                }

            }
            db.query.execute("INSERT INTO Cuentas"
                    + " (usuario,contra,id)"
                    + " VALUES ('" + textf_usuario_crear.getText() + "',  '" + textf_contra_crear.getText() + "', '" + index + "')");
            db.commit();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        db.desconectar();
    }

